package com.hcl.loops;

public class ForWhileDemo {

	public static void main(String[] args) {
		for(int i=10;i<10;i++)
		{
			System.out.println("For Loop : "+i);
		}
		
		int i=10;
		do {
			System.out.println("Do..While Loop "+i);
			i++;
		}while(i<10);
		
		int j=10;
		while(j<10)
		{
			System.out.println("While Loop "+j);
			j++;
		}

	}

}
